import { CollectionConfig } from 'payload/types'

const isEditor = ({ req: { user } }: any) =>
  !!user && ['super-admin', 'admin', 'content-editor'].includes(user.role)

// ─── Team Members ───────────────────────────────────────────────────────────
export const TeamMembers: CollectionConfig = {
  slug: 'team-members',
  admin: {
    useAsTitle: 'name',
    group: 'Content',
    defaultColumns: ['name', 'role', 'hub', 'order'],
  },
  access: {
    read:   () => true,
    create: isEditor,
    update: isEditor,
    delete: ({ req: { user } }) => !!user && ['super-admin', 'admin'].includes(user.role),
  },
  fields: [
    { name: 'name',  type: 'text', required: true },
    { name: 'role',  type: 'text', required: true, admin: { description: 'e.g. Co-Founder & CEO' } },
    {
      name: 'hub',
      type: 'select',
      options: [
        { label: 'Vienna (HQ)',         value: 'vienna' },
        { label: 'Dubai (MENA)',        value: 'dubai' },
        { label: 'Dhaka (Production)',  value: 'dhaka' },
      ],
    },
    { name: 'bio',    type: 'textarea' },
    { name: 'photo',  type: 'upload', relationTo: 'media' },
    { name: 'linkedin', type: 'text', admin: { description: 'Full LinkedIn URL' } },
    { name: 'order',  type: 'number', defaultValue: 0 },
  ],
}

