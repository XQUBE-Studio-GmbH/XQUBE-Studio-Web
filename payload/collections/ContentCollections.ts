// ─── Blog Posts ──────────────────────────────────────────────────────────────
export const BlogPosts: CollectionConfig = {
  slug: 'blog-posts',
  admin: {
    useAsTitle: 'title',
    group: 'Content',
    defaultColumns: ['title', 'author', 'status', 'publishedAt', 'updatedAt'],
    preview: (doc) => `${process.env.NEXT_PUBLIC_SITE_URL}/blog/${doc.slug}`,
    description: 'Blog & Insights. Phase 2: Pritom will auto-generate and publish articles here.',
  },
  access: {
    read: ({ req: { user } }) => {
      if (user) return true
      return { status: { equals: 'published' } }
    },
    create: isEditor,
    update: isEditor,
    delete: ({ req: { user } }) => !!user && ['super-admin', 'admin'].includes(user.role),
  },
  versions: {
    drafts: true,
  },
  fields: [
    { name: 'title', type: 'text', required: true },
    { name: 'slug',  type: 'text', required: true, unique: true },
    {
      name: 'author',
      type: 'relationship',
      relationTo: 'users',
    },
    {
      name: 'coverImage',
      type: 'upload',
      relationTo: 'media',
    },
    {
      name: 'excerpt',
      type: 'textarea',
      admin: { description: 'Short summary shown on blog listing page and in SEO description.' },
    },
    {
      name: 'content',
      type: 'richText',
    },
    {
      name: 'tags',
      type: 'array',
      fields: [{ name: 'tag', type: 'text' }],
    },
    {
      name: 'status',
      type: 'select',
      defaultValue: 'draft',
      options: [
        { label: 'Published', value: 'published' },
        { label: 'Draft',     value: 'draft' },
      ],
    },
    {
      name: 'publishedAt',
      type: 'date',
      admin: { description: 'Leave blank to use current date on publish.' },
    },
    {
      name: 'generatedByPritom',
      type: 'checkbox',
      defaultValue: false,
      admin: {
        description: 'Phase 2: Mark if this article was generated by Pritom AI.',
        position: 'sidebar',
      },
    },
    {
      name: 'seo',
      label: 'SEO',
      type: 'group',
      fields: [
        { name: 'title', type: 'text' },
        { name: 'description', type: 'textarea' },
        { name: 'ogImage', label: 'OG Image', type: 'upload', relationTo: 'media' },
      ],
    },
  ],
}
